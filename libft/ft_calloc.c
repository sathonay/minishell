#include "libft.h"
#include <stdint.h>

void	*ft_calloc(size_t count, size_t size)
{
	char	*ptr;

	size *= count;
	ptr = malloc(sizeof(char) * size);
	if (!ptr)
		return (NULL);
	while (size--)
		ptr[size] = '\0';
	return (ptr);
}
